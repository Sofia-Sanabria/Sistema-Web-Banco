    /**
     * Método que realiza la animación de despliegue de las opciones del apartado
     * para las Tarjetas de crédito
     * @param evt Objeto MouseEvent que representa la acción del mouse, los clicks
     * y la entrada dentro de los dominios de los widgets 
     */
    private void jLabelBotonTarjetasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelBotonTarjetasMouseClicked
        Thread hiloPausar = new Thread(){
            @Override
            public void run(){
                if(!jLabelBotonTransacciones.isOpaque()){
                    jLabelBotonTransaccionesMouseClicked(null);
                    try {
                        Thread.sleep(150);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

                //si el widget por debajo del jPanelTarjetas se encuentra en su lugar
                //desplaza y despliega las opciones
                if(jPanelTransacciones.getY() <= 330){
                    desplazarAbajoY(jPanelTransacciones, 330+100, 1);
                    desplazarAbajoY(jPanelDeposito, 380+100, 1);
                    desplazarAbajoY(jPanelPagos, 430+100, 1);
                    desplazarAbajoY(jPanelHistorial, 480+100, 1); 
                    desplegarY(jPanelOpcionesTarjetas, 100, 1);
                    jLabelBotonTarjetas.setText("Tarjetas              –");
                    jLabelBotonTarjetas.setOpaque(false);
                //sino las devuelve a su estado original
                }else if(jPanelTransacciones.getY() > 330){
                    desplazarArribaY(jPanelTransacciones, 430-100, 1);
                    desplazarArribaY(jPanelDeposito, 480-100, 1);
                    desplazarArribaY(jPanelPagos, 530-100, 1);
                    desplazarArribaY(jPanelHistorial, 580-100, 1); 
                    plegarY(jPanelOpcionesTarjetas, jPanelOpcionesTarjetas.getHeight(), 1);
                    jLabelBotonTarjetas.setText("Tarjetas              +");
                    jLabelBotonTarjetas.setOpaque(true);
                }
                jLabelBotonTarjetas.repaint();
            }
        }; hiloPausar.start();
        this.repaint();//refresca el jFrame
    }//GEN-LAST:event_jLabelBotonTarjetasMouseClicked
    
