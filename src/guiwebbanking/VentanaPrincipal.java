    /**
     * Método que utiliza hilos para la animación de desplazar jPanels hacia abajo
     * @param jpanel Variable tipo JPanel a desplazar
     * @param finalY Posicion final del JPanel en el eje Y
     * @param time   Variable tipo int para realizar los sleeps en el ciclo
     */
    private void desplazarAbajoY(JPanel jpanel, int finalY, int time){
        Thread hiloDesplazar = new Thread() { //declara un nuevo hilo
            @Override
            public void run(){ //sobreescrible el método run de la clase Thread
                //ciclo en donde se relocaliza el widget con un contador
                for(int desplazarY = jpanel.getY(); desplazarY <= finalY; desplazarY++){
                    try {
                        Thread.sleep(time);
                        jpanel.setLocation(jpanel.getX(), desplazarY); //relocaliza el JPanel
                    } catch (InterruptedException ex) {
                        Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }; hiloDesplazar.start(); //inicia el hilo
    }
    
