    /**
     * Método que cambia la dimensión de un JPanel simulando su despliegue en pantalla
     * @param jpanel          JPanel a ser desplegado
     * @param totalDesplegarY Parámetro tipo int que determina cuanto se sumará a la
     * altura actual del JPanel
     * @param time            Milisegundos para simular los intervalos de la animación
     */
    private void desplegarY(JPanel jpanel, int totalDesplegarY, int time){
        Thread hiloDesplegar = new Thread() { //declara un nuevo hilo
            @Override
            public void run(){
                int alturaActual = jpanel.getHeight();
                //ciclo en donde se redimensiona el widget
                for(int desplegarY = alturaActual; desplegarY <= alturaActual+totalDesplegarY; desplegarY++){
                    try {
                        Thread.sleep(time);
                        jpanel.setSize(new Dimension(jpanel.getWidth(), desplegarY)); //redimensiona el JPanel
                    } catch (InterruptedException ex) {
                        Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }; hiloDesplegar.start(); //inicia el hilo
    }
    
    
    /**
     * Método que cambia la dimensión de un JPanel simulando su plegado
     * @param jpanel JPanel a ser plegado
     * @param totalPlegarY Parámetro que representa el total a ser restado de la
     * altura actual del JPanel
     * @param time   Milisegundos para simular los intervalos de la animación
     */
    private void plegarY(JPanel jpanel, int totalPlegarY, int time){
        Thread hiloplegar = new Thread() { //declara un nuevo hilo
            @Override
            public void run(){ 
                int alturaActual = jpanel.getHeight();
                //ciclo en donde se disminuye la dimensión del JPanel de uno en uno
                for(int plegarY = alturaActual; plegarY >= alturaActual-totalPlegarY; plegarY--){ 
                    //captura la excepción InterruptedException generada por .sleep()
                    try {
                        Thread.sleep(time);
                        jpanel.setSize(new Dimension(jpanel.getWidth(), plegarY)); //redimensiona el JPanel
                    } catch (InterruptedException ex) {
                        Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }; hiloplegar.start(); //comienza el hilo
    }    
    
